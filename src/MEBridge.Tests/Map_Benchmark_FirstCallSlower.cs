public class Benchmark_Tests
{
    [Fact]
    public async Task Map_FirstCall_ShouldTakeLongerThanSecondDueToCaching()
    {
        var warmupMapper = TestHelpers.CreateMapper();
        await warmupMapper.BridgeTo<BenchWarmup_M>(new BenchWarmup_E { A = "w" }, null);

        var entity = new BenchBig_Entity();
        for (int i = 0; i <= 99; i++)
            typeof(BenchBig_Entity).GetProperty($"Prop{i:D3}")!.SetValue(entity, $"v{i}");

        long minFirst = long.MaxValue, minSecond = long.MaxValue;

        for (int trial = 0; trial < 5; trial++)
        {
            var mapper = TestHelpers.CreateMapper();

            var sw = Stopwatch.StartNew();
            var r1 = await mapper.BridgeTo<BenchBig_Model>(entity, null);
            sw.Stop();
            if (sw.ElapsedTicks < minFirst) minFirst = sw.ElapsedTicks;

            sw.Restart();
            var r2 = await mapper.BridgeTo<BenchBig_Model>(entity, null);
            sw.Stop();
            if (sw.ElapsedTicks < minSecond) minSecond = sw.ElapsedTicks;

            Assert.NotNull(r1);
            Assert.NotNull(r2);
        }

        Assert.True(minFirst >= minSecond,
            $"Min first call ({minFirst} ticks) should be >= min second ({minSecond} ticks)");
    }
}
